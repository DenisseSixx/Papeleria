    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
      if (evt.getButton() == 1) {
            txtClave.setEnabled(false);
            int fila = TablaDatos.getSelectedRow();

          
            String fe = TablaDatos.getValueAt(fila, 5).toString();
            DateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            try {
                Date fecha = formato.parse(fe);
                txtFecha.setDate(fecha);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
            }
       

            txtClave.setText(TablaDatos.getValueAt(fila, 0).toString());
            txtTitulo.setText(TablaDatos.getValueAt(fila, 1).toString());
            txtStock.setText(TablaDatos.getValueAt(fila, 2).toString());
            txtNopags.setText(TablaDatos.getValueAt(fila, 3).toString());

            txtNoRegistro.setText(TablaDatos.getValueAt(fila, 4).toString());
            txtBarras.setText(TablaDatos.getValueAt(fila, 5).toString());
            txtClaveAutor.setText(TablaDatos.getValueAt(fila, 7).toString());
            nombreImagen = TablaDatos.getValueAt(fila, 6).toString();

            int clv = Integer.parseInt(txtClave.getText());
            ImageIcon img = new NegocioLibros().puenteObtenerImagen(clv);
            Icon icono = new ImageIcon(img.getImage().getScaledInstance(lbImagen.getWidth(), lbImagen.getHeight(), Image.SCALE_DEFAULT));
            if (img != null) {
                lbImagen.setIcon(icono);
            }

            btnEliminar.setEnabled(true);
            btnModificar.setEnabled(true);
            btnAgregar.setEnabled(false);
 
      
     }
    }//GEN-LAST:event_TablaDatosMouseClicked

