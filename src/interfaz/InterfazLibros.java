boolean verificar(){
    boolean v;
    if(txtClave.getText().isEmpty() || txtTitulo.getText().isEmpty() || txtBarras.getText().isEmpty() || txtStock.getText().isEmpty() || txtNopags.getText().isEmpty()
            || lbImagen.getIcon()== null || txtNoRegistro.getText().isEmpty() || txtFecha.getDate()==null|| txtClaveAutor.getText().isEmpty() || txtNombreAutor.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Llenar todos los datos", "Falta de datos",JOptionPane.INFORMATION_MESSAGE);
        v=false;
    }else
        v=true;
return v;

}
