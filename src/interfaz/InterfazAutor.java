    private void txtNomProMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtNomProMouseClicked
        try {
        Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/biblioteca","root","");
        PreparedStatement pst = conexion.prepareStatement("select * from tbautor where claveautor =?");
        pst.setString(1,txtClave.getText().trim());
        ResultSet rs = pst.executeQuery();
        if(rs.next()){
        txtClave.setText(rs.getString("claveautor"));
        txtNomPro.setText(rs.getString("nombre"));
        txtRFC.setText(rs.getString("rfc"));
        txtTelPro.setText(rs.getString("telefono"));
        txtDPro.setText(rs.getString("direccion"));
        txtCorreo.setText(rs.getString("correo"));
    }else{
        JOptionPane.showMessageDialog(null, "Autor No Registrado");
    }
       }catch(Exception e){
        JOptionPane.showMessageDialog(null,"Error "+e.getMessage());
       }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtNomProMouseClicked
  void Limpiar() {
        txtClave.setText("");
        txtNomPro.setText("");
        txtRFC.setText("");
        txtTelPro.setText("");
        txtDPro   .setText("");
        txtCorreo.setText("");
    }

    boolean Verificar() {
        boolean v;
        if (txtClave.getText().isEmpty()
                || txtNomPro.getText().isEmpty()
                || txtRFC.getText().isEmpty()
                || txtTelPro.getText().isEmpty()
                || txtDPro.getText().isEmpty()
                || txtCorreo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Se deben llenar todos los datos", "Faltan datos", JOptionPane.INFORMATION_MESSAGE);
            v = false;
        } else {
            v = true;
        }
        return v;
    }


  



