    private void txtNomProKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomProKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();
        boolean mayusculas = key>=65 && key<=90;
        boolean minusculas = key>=97 && key<=122;
        
        boolean espacio = key==32;
        
        if(!(minusculas||mayusculas||espacio)){
            
            evt.consume();   
        }        
    }//GEN-LAST:event_txtNomProKeyTyped

    private void txtTelProKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelProKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();
        boolean numeros = key>=48 && key <=57;
        if(!numeros){
            evt.consume();
        }
        if(txtTelPro.getText().trim().length()==10){
            evt.consume();
        }
        
        
    }//GEN-LAST:event_txtTelProKeyTyped

