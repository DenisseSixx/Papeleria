    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
         if (Verificar()) {
            int n = JOptionPane.showConfirmDialog(null, "Seguro que deseas eliminar?: ");
            if (n == 0) {
                Autores prov = new Autores();
                prov.setClaveautor(Integer.parseInt(txtClave.getText()));
                try {
                    new NegocioAutor().PuenteEliminar(prov);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Error: " + ex, "Verificacion", JOptionPane.ERROR_MESSAGE);
                }
                Limpiar();
                 LlenarTabla();
            }
        }                                          

    }//GEN-LAST:event_btnEliminar1ActionPerformed

