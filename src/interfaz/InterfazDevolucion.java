    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        if (evt.getButton() == 1) {
            int fila = TablaDatos.getSelectedRow();
            txtClaveProd.setText(TablaDatos.getValueAt(fila, 0).toString());
            txtNombreProd.setText(TablaDatos.getValueAt(fila, 1).toString());
            txtStock.setText(TablaDatos.getValueAt(fila, 2).toString());
            txtClaveProv.setText(TablaDatos.getValueAt(fila, 3).toString());
        
        }
    }//GEN-LAST:event_TablaDatosMouseClicked

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Libros prod = new Libros();
        NegocioDevolucion nm = new NegocioDevolucion();

        int stock = Integer.parseInt(txtStock.getText());
        int cant = Integer.parseInt(txtCantidad.getText());

        stock = stock + cant;

        prod.setClave(Integer.parseInt(txtClaveProd.getText()));
        prod.setStock(stock);
        nm.PuenteDevoluvion(prod);
        
        LlenarTabla();
    }//GEN-LAST:event_btnAceptarActionPerformed

